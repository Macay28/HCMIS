                    <h5 class="fst-italic">► <strong>What are the symptoms of cough?</strong></h5>
                    <h5>The symptoms that come with a cough will vary. It depends on what is causing your cough.
                        You can have a productive (chesty) or non-productive (dry) cough. A non-productive cough
                        does not have any mucus or phlegm. And the productive or wet cough is a type of cough that
                        makes mucus. It may cause a feeling of congestion or tightness in your chest. When you have
                        a productive cough, you cough up mucus. This is known as phlegm (the word sounds like 'flem').
                        The phlegm comes up from your lungs and lower airways. A productive cough is not necessarily
                        a sign of a more serious illness. You may not need antibiotics or other treatments. However,
                        a productive cough can sometimes indicate a serious problem like pneumonia or heart failure.
                    </h5>
                    <br><br><br><br><br><br>
                    <h5 class="fst-italic">► <strong>When to see a doctor?<br>You should seek medical care if you:</strong></h5>
