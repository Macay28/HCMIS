                    <h5 class="fst-italic">► <strong>Types of cough:</strong></h5>
                    <h5><strong>1. DRY COUGHS</strong> - commonly follow on from respiratory illnesses, such as colds and the flu.
                        These coughs develop when there is little or no mucus in the throat. A person may feel a
                        tickling sensation in their throat and be unable to stop coughing.
                        There are other causes that people can investigate if a cough becomes chronic:</h5>
                    <br>
                    <h5 class="fst-italic">• <strong>Asthma:</strong> Other symptoms include a tight sensation of the chest,
                        shortness of breath, and wheezing.</h5> <br>
                    <h5 class="fst-italic">• <strong>Gastroesophageal reflux disease (GERD):</strong> This is when
                        stomach acid travels up towards the throat, which can trigger a cough.</h5>
                    <h5 class="fst-italic">• <strong>Lung cancer:</strong> A cough that is related to lung cancer
                        may coincide with blood in the mucus. It is rare that a cough is due to lung cancer, but
                        if a person is concerned, they should see a doctor.</h5> <br>
                    <br>
                    <h5 class="fst-italic">► <strong>Treatment:</strong></h5>
                    <h5>A person can ease the tickling sensation of a dry cough by drinking water,
                        taking a cough drop, or using cough syrup.</h5>
                    <br><br>
                    <h5><strong>2. WET COUGHS</strong> - people might describe a wet cough as a chesty cough.
                        This cough occurs when a person coughs up mucus or phlegm. Wet coughs are typically
                        due to an infection, such as the flu, the common cold, or a chest infection.
                        A person with a chest infection may cough up phlegm that contains small amounts of
                        bright red blood. This blood comes from the lungs and is typically nothing to worry about.
                        If a person finds themselves coughing up blood that is dark and contains food, or what
                        resembles coffee grounds, they should seek medical help. Some wet coughs can be chronic
                        and may be due to:
                    </h5>
                    <br><br>
                    <h5 class="fst-italic">• <strong>Bronchiectasis:</strong> A condition resulting from mucus pools in small pouches
                        in the lungs that the body is unable to clear.</h5><br>
                    <h5 class="fst-italic">• <strong>Pneumonia:</strong> This is when a bacterial infection causes the tissue
                        on the lungs to become inflamed.</h5>
                    <h5 class="fst-italic">• <strong>Nontuberculous mycobacteria infection:</strong> This is noncontagious and
                        can be accompanied by tiredness, feeling unwell, and weight loss.</h5>
                    <h5 class="fst-italic">• <strong>Chronic obstructive pulmonary disease (COPD):</strong> This is a type of lung
                        disease where common symptoms may include shortness of breath and wheezing.</h5>
                    <br><br>
                    <h5 class="fst-italic">► <strong>Treatment:</strong></h5>
                    <h5>Staying hydrated can help a wet cough stay productive and ease the symptoms of a cold.
                        Some people also find relief from over-the-counter (OTC) cough remedies, such as cough
                        drops, chest rubs, and pain relievers. If a bacterial infection is causing the cough,
                        a person may need antibiotics.</h5>
                    <br><br><br><br><br>
                    <h5><strong>3. WHOOPING COUGH PERTUSSIS</strong> - better known as whooping cough, is a highly
                        contagious bacterial infection. Newborns and people who have not had a vaccination may develop
                        this illness. A person with whooping cough typically has mild cold or flu-like symptoms,
                        followed by an aggressive and painful cough. People with weak immune systems, such as babies,
                        may struggle to fight the infection or have trouble breathing. Those with this infection are most
                        likely to pass it on for roughly 2 weeks from when they begin coughing. The best protection against
                        the illness is a whooping cough vaccination.
                    </h5>
                    <br><br>
                    <h5 class="fst-italic">► <strong>Treatment:</strong></h5>
                    <h5>Taking antibiotics early can decrease the severity of whooping cough, so an unvaccinated
                        person should see a doctor as soon as possible if symptoms develop.</h5>
                    <br><br>
                    <h5><strong>4. CHRONIC COUGH</strong> - is a cough that lasts longer than a typical illness,
                        usually 8 weeks or more. These coughs sometimes signal an underlying disease. A person
                        should see a doctor for appropriate diagnosis and treatment. <br> <br><br><br>
                        Some potential causes of a long-term cough include:
                    </h5>
